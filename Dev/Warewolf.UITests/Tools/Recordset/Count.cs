        [TestMethod]
        [TestCategory("Recordset Tools")]
        public void CountTool_Small_And_LargeView_UITest()
        {
            Assert.IsTrue(ToolsUIMap.MainStudioWindow.DockManager.SplitPaneMiddle.TabManSplitPane.TabMan.WorkflowTab.WorkSurfaceContext.WorkflowDesignerView.DesignerView.ScrollViewerPane.ActivityTypeDesigner.WorkflowItemPresenter.Flowchart.CountRecordset.Exists, "Count tool on the design surface does not exist");
