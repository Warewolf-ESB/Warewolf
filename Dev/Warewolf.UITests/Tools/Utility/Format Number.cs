using Microsoft.VisualStudio.TestTools.UITesting;
using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace Warewolf.UITests.Tools
{
    [CodedUITest]
    public class Format_Number
    {
        [TestMethod]
		[TestCategory("Tools")]
        public void FormatNumberToolUITest()
        {
            UIMap.Drag_Toolbox_Format_Number_Onto_DesignSurface();
            UIMap.Open_NumberFormat_Toolbox_Large_View();
            UIMap.Enter_Number_To_Format();
            UIMap.Select_Round_Up();
            UIMap.Enter_Decimals_To_Show();
            UIMap.Enter_Result_Variable();
            UIMap.Click_FormatNumber_Done_Button();
            UIMap.Press_F6();
        }

        #region Additional test attributes

        [TestInitialize]
        public void MyTestInitialize()
        {
            UIMap.SetPlaybackSettings();
#if !DEBUG
            UIMap.CloseHangingDialogs();
#endif
            UIMap.InitializeABlankWorkflow();
        }
        [TestCleanup]
        public void MyTestCleanup()
        {
            UIMap.Click_Close_Workflow_Tab_Button();
            UIMap.Click_MessageBox_No();
        }
        /// <summary>
        ///Gets or sets the test context which provides
        ///information about and functionality for the current test run.
        ///</summary>
        public TestContext TestContext
        {
            get
            {
                return testContextInstance;
            }
            set
            {
                testContextInstance = value;
            }
        }

        private TestContext testContextInstance;

        UIMap UIMap
        {
            get
            {
                if ((_UIMap == null))
                {
                    _UIMap = new UIMap();
                }

                return _UIMap;
            }
        }

        private UIMap _UIMap;

        #endregion
    }
}
