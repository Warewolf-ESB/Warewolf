{
    /// <summary>
    /// A <see cref="DataPresenterBase"/> derived control that uses a <see cref="GridView"/> and an associated <see cref="GridViewPanel"/> to arrange <see cref="DataRecord"/>s in a classic grid layout.  
    /// </summary>
    /// <remarks>
    /// <p class="body">The XamDataGrid is essentially a <see cref="XamDataPresenter"/> that implements a single view i.e., a <see cref="GridView"/>.  It contains the same functionality
    /// as a <see cref="XamDataPresenter"/> that has its <see cref="XamDataPresenter.View"/> property set to an instance of a <see cref="GridView"/>, without the ability to dynamically switch between views.</p>
    /// <p class="body">If you need the classic grid layout provided by a <see cref="GridView"/> and don't need the ability to dynamically switch to other views, you should use the
    /// XamDataGrid rather than the <see cref="XamDataPresenter"/>.  It's a bit more convenient to use than the <see cref="XamDataPresenter"/> since it exposes a <see cref="XamDataGrid.ViewSettings"/> property
    /// of type <see cref="GridViewSettings"/> directly off the control for easy manipulation of the properties that control all aspects of
    /// grid layout.</p>
    /// <p class="body">Refer to the <see cref="GridViewSettings"/> topic for detailed information on all available properties.</p>
	/// <p class="body">Refer to the <a href="xamData_TheoryOfOperation.html">Theory of Operation</a> topic in the Developer's Guide for an explanation of how this control operates.</p>
    /// <p class="body">Refer to the <a href="WPF_Working_with_xamDataGrid_xamDataCarousel_and_xamDataPresenter_Styling_Points.html">Working with xamDataGrid, xamDataCarousel and xamDataPresenter Styling Points</a> topic in the Developer's Guide for an explanation of how to style the control.</p>
	/// </remarks>
    /// <seealso cref="XamDataPresenter"/>
    /// <seealso cref=" XamDataPresenter.View"/>
    /// <seealso cref="GridView"/>
    /// <seealso cref="DataRecord"/>
    [ContentProperty("DataItems")]
	[TemplatePart(Name = "PART_ContentSiteGrid", Type = typeof(Grid))]
	
	//[ToolboxItem(true)]
	
	//[System.Drawing.ToolboxBitmap(typeof(XamDataGrid), AssemblyVersion.ToolBoxBitmapFolder + "XamDataGrid.bmp")]
	//[Description("A DataPresenterBase derived control that uses a GridViewPanel to arrange its items in a traditional tabular layout, eiher vertically or horizontally.  The XamDataGrid uses elements generated by the XamDataPresenterBase to display each item in the list.")]
	// AS 4/15/10 The base class has this set to false so DataPresenterBase doesn't show up but we need to set it back to true for the derived controls that should show up.
	[DesignTimeVisible(true)]	// JM 02-18-10 - DO NOT MOVE TO DESIGN ASSEMBLY!!!
	public class XamDataGrid : DataPresenterBase
	{
		#region Member variables

		private UltraLicense							_license = null;
		private GridViewSettings						_viewSettings = new GridViewSettings();

		#endregion //Member variables	
    
		#region Constructor

		static XamDataGrid()
		{
			FrameworkElement.DefaultStyleKeyProperty.OverrideMetadata(typeof(XamDataGrid), new FrameworkPropertyMetadata(typeof(XamDataGrid)));

			//			KeyboardNavigation.DirectionalNavigationProperty.OverrideMetadata(typeof(DataPresenterBase), new FrameworkPropertyMetadata(KeyboardNavigationMode.Cycle));


			// Load our commands.  (Since our commands are defined as statics and static initializers are not called
			// until the class is accessed, call the convenience method 'LoadCommands' which will access the class
			// and force the loading of our static commands and register command bindings)
			DataPresenterCommands.LoadCommands();
		}

		/// <summary>
		/// Initializes a new instance of the <see cref="XamDataGrid"/> class
		/// </summary>
		public XamDataGrid()
		{
			// verify and cache the license
			//
			// Wrapped in a try/catch for a FileNotFoundException.
			// When the assembly is loaded dynamically, VS seems 
			// to be trying to reload a copy of Shared even though 
			// one is in memory. This generates a FileNotFoundException
			// when the dll is not in the gac and not in the AppBase
			// for the AppDomain.
			//
			// AS 11/7/07 BR21903
			// Always do the license checks.
			//
			//if (DesignerProperties.GetIsInDesignMode(this))
			{
				try
				{
					// We need to pass our type into the method since we do not want to pass in 
					// the derived type.
					this._license = LicenseManager.Validate(typeof(XamDataGrid), this) as UltraLicense;
				}
				catch (System.IO.FileNotFoundException) { }
			}


			// Create our view and set it on DataPresenterBase.
			this.CurrentViewInternal = new GridView();
		}

		#endregion //Constructor

		#region Base class overrides

			#region OnInitialized

		/// <summary>
		/// Called after the element has been initialized.
		/// </summary>
		protected override void OnInitialized(EventArgs e)
		{
			base.OnInitialized(e);

			// Set the ViewSettings dependency property using the CLR property which will do a lazy create of the object.
            // JJD 1/2/07 
            // Check for design mode first
            // If we are in design mode don't sync ViewSettings
            if (DesignerProperties.GetIsInDesignMode(this) == false)
                this.SetValue(XamDataGrid.ViewSettingsProperty, this.ViewSettings);

			// Bind the current view's ViewSettings property to our ViewSettings property.
			this.CurrentViewInternal.SetBinding(GridView.ViewSettingsProperty, Utilities.CreateBindingObject(XamDataGrid.ViewSettingsProperty, BindingMode.OneWay, this));
		}

			#endregion //OnInitialized	

			#region OnPropertyChanged

		/// <summary>
		/// Called when a dependency property has changed
		/// </summary>
		/// <param name="e">The arguments identifying the property and containing the new and old values.</param>
		protected override void OnPropertyChanged(DependencyPropertyChangedEventArgs e)
		{
			base.OnPropertyChanged(e);

            // AS 3/11/09 Optimization
			//switch (e.Property.Name)
			//{
			//	case "ViewSettings":
            if (e.Property == ViewSettingsProperty)
            {
					// Set our ViewSettings member variable.
					this._viewSettings = e.NewValue as GridViewSettings;

					//break;
			}
		}

			#endregion //OnPropertyChanged	

		#endregion //Base class overrides

		#region Properties

			#region Public properties

				#region ViewSettings

		/// <summary>
		/// Identifies the <see cref="ViewSettings"/> dependency property
		/// </summary>
		public static readonly DependencyProperty ViewSettingsProperty = DependencyProperty.Register("ViewSettings",
			typeof(GridViewSettings), typeof(XamDataGrid), new FrameworkPropertyMetadata(null, new CoerceValueCallback(OnCoerceViewSettings)));

		private static object OnCoerceViewSettings(DependencyObject d, object value)
		{
            if (value == null)
            {
                // JJD 1/2/07 
                // Check for design mode first
                // Only create a ViewSettings if we are in run mode 
                if (DesignerProperties.GetIsInDesignMode(d) == false)
                    return new GridViewSettings();
            }

			return value;
		}

		/// <summary>
		/// Returns/set the <see cref="GridViewSettings"/> object for this <see cref="XamDataGrid"/>.
		/// </summary>
        /// <remarks>
        /// <p class="body">The <see cref="GridViewSettings"/> object exposes properties that let you control all aspects of grid layout.
        /// Refer to the <see cref="GridViewSettings"/> topic for detailed information on all available properties.</p>
        /// </remarks>
        /// <seealso cref="ViewSettingsProperty"/>
		//[Description("Returns/set the GridViewSettings object for this XamDataGrid.")]
		//[Category("Appearance")]
		[Bindable(true)]
		public GridViewSettings ViewSettings
		{
			get
			{
                // JJD 1/2/07 
                // Check for design mode first
                // If we are in design mode don't create an instance of the Settings object
                //if (this._viewSettings == null)
                if (this._viewSettings == null &&
                    DesignerProperties.GetIsInDesignMode(this) == false)
                    this._viewSettings = new GridViewSettings();

				return this._viewSettings;
			}
			set
			{
				this.SetValue(XamDataGrid.ViewSettingsProperty, value);
			}
		}

		/// <summary>
		/// Determines if the <see cref="ViewSettings"/> property needs to be serialized.
		/// </summary>
		/// <returns>True if the property should be serialized</returns>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public bool ShouldSerializeViewSettings()
		{
			return this.ViewSettings != (GridViewSettings)XamDataGrid.ViewSettingsProperty.DefaultMetadata.DefaultValue;
		}

		/// <summary>
		/// Resets the <see cref="ViewSettings"/> property to its default value.
		/// </summary>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public void ResetViewSettings()
		{
			this.ViewSettings = (GridViewSettings)XamDataGrid.ViewSettingsProperty.DefaultMetadata.DefaultValue;
		}

				#endregion //ViewSettings

			#endregion //Public properties

		#endregion //Properties
	}
}

